import java.util.*;

//Clase enfermero
public class enfermero{
  //atributos
  public int id;
  public String nombre;
  public String apellido;
  public int estudios;
  public int experiencia;

  //Constructor
  public enfermero(int ide, String Nombre, String Apellido, int Estudios, int Experiencia){
    id = ide;
    nombre = Nombre;
    apellido = Apellido;
    estudios = Estudios;
    experiencia = Experiencia;
  }
}
